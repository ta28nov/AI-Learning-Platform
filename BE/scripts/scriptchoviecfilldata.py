    for i, ls in enumerate(lessons_list): ls["order"] = i + 1
    return {"title": title, "description": desc, "order": 0,
            "difficulty": diff, "estimated_hours": hours,
            "learning_outcomes": outcomes, "lessons": lessons_list}

